/* ----------------------------------- == ----------------------------------- */
// loose equal | double equal
// Memeriksa kesetaraan value kanan dengan value kiri
// JS akan mencoba menyamakan tipe data kedua value-nya
console.log(10 == 100);
console.log(10 == 10);
console.log(null == 100);
console.log("1" == 1);

/* ----------------------------------- === ---------------------------------- */
// strict equal | triple equal
// Memeriksa kesetaraan value kanan dengan value kiri
// JS TIDAK akan mencoba menyamakan tipe data kedua value-nya
console.log("1" === 1);

/* ----------------------------------- != ----------------------------------- */
console.log(10 != 10);
console.log(1 != 10);

/* ----------------------------------- !== ---------------------------------- */
console.log(100 !== 10);

/* ------------------------------------ > ----------------------------------- */
// greater than
console.log(10 > 5);
console.log(10 > 10);

/* ----------------------------------- >= ----------------------------------- */
// greater than or equal
console.log(10 >= 5);
console.log(10 >= 10);

/* ------------------------------------ < ----------------------------------- */
// less than
console.log(10 < 50);
console.log(10 < 10);

/* ----------------------------------- <= ----------------------------------- */
// less than or equal
console.log(10 <= 50);
console.log(10 <= 10);
