let age = 20;

// age = age + 5;
age += 5;
console.log(age);

// age = age - 10;
age -= 10;
console.log(age);

// age = age * 10;
age *= 10;
console.log(age);

// age = age ** 2;
age **= 2;
console.log(age);

// age = age / 10;
age /= 10;
console.log(age);

/* -------------------------- Increment & Decrement ------------------------- */
let score = 10;

// score = score + 1;
// score += 1;
score++;
console.log(score);

// score = score - 1;
// score -= 1;
score--;
console.log(score);

/* ---------------------------- Postfix & Prefix ---------------------------- */
let point = 50;
point++; // postfix
console.log(point);

let value = 50;
++value; // prefix
console.log(value);

let height = 100;
let width = 100;

console.log(height++);
console.log(height);

console.log(++width);
console.log(width);
